package codeNinjas;

public class DataTypesStorage {
	public static void main(String[] args) {
		int min= Integer.MIN_VALUE;
		int max= Integer.MAX_VALUE;
		System.out.println("integer min value: " + min);
		System.out.println("integer max value: " + max);
		
		float min_f = Float.MIN_VALUE;
		float max_f = Float.MAX_VALUE;
		System.out.println("float min value: "+ min_f);
		System.out.println("float max value: "+ max_f);
		
		short min_s = Short.MIN_VALUE;
		short max_s = Short.MAX_VALUE;
		System.out.println("short min value: "+ min_s);
		System.out.println("short max value: "+ max_s);
		
	}

}
